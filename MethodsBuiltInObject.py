

#Append method to add or combine two list
mylist = [ 1,2,3]
mylist.append(4)
print(mylist)#[1, 2, 3, 4]

#Pop method remove the last item if the list
mylist.pop()
print(mylist)#[1, 2, 3]


#How to get list of a method in python, write your list name and press dot will give all method
mylist.insert(0,6)
print(mylist)#[6, 1, 2, 3 ]
mylist.insert(1,6)
print(mylist)#[6, 6, 1, 2, 3]







