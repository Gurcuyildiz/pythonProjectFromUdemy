
#Hexadecimal
#we write hex and give any number then it will give us the hexadecimal equivalence
hex(12)# 0xc
hex(512)#0x200



#Binary
#this will give binary equivalence of this number
bin(1234)#0b10011010010
bin(128)#0b10000000

#with two argument => equivalent to x**y
#with three argument => equivalent to (x**y) % z
pow(2,4,3) #1


#gives the absolute value of a number
abs(-3) #3
abs(2)#2

#always return floating point number
round(3.1)#2
round(3.9) #4.0
#you can specify how many digit you want to see to add second argument
#here i want to see two digit
round(3.14,2)#3.14

